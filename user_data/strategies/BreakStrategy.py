    def populate_indicators(self, dataframe: DataFrame, metadata: dict) -> DataFrame:
        """
        Adds several different TA indicators to the given DataFrame

        Performance Note: For the best performance be frugal on the number of indicators
        you are using. Let uncomment only the indicator you are using in your strategies
        or your hyperopt configuration, otherwise you will waste your memory and CPU usage.
        :param dataframe: Dataframe with data from the exchange
        :param metadata: Additional information, like the currently traded pair
        :return: a Dataframe with all mandatory indicators for the strategies
        """
        dataframe['lowest'] = 0
        dataframe['highest'] = 0
        dataframe['signal'] = 0
        lowest = None
        for index, row in dataframe.iterrows():
            if not lowest:
                lowest = row['low']
                continue

            if row['low'] < lowest:
                lowest = row['low']
                dataframe.loc[index, 'lowest'] = 1
                continue

            if (row['low'] > lowest) & (dataframe.loc[ (index - 1), 'lowest'] == 1) :
                dataframe.loc[index, 'signal'] = 1
                lowest = None

        highest = None
        for index, row in dataframe.iterrows():

            if not highest:
                highest = row['high']
                continue

            if row['high'] > highest:
                highest = row['high']
                dataframe.loc[index, 'highest'] = 1
                continue

            if (row['high'] < highest) & (dataframe.loc[ (index - 1), 'highest'] == 1) :
                dataframe.loc[index, 'signal'] = -1
                highest = None

        return dataframe

    def populate_buy_trend(self, dataframe: DataFrame, metadata: dict) -> DataFrame:
        """
        Based on TA indicators, populates the buy signal for the given dataframe
        :param dataframe: DataFrame populated with indicators
        :param metadata: Additional information, like the currently traded pair
        :return: DataFrame with buy column
        """
        dataframe.loc[(dataframe['signal'] == 1), 'buy'] = 1
        return dataframe

    def populate_sell_trend(self, dataframe: DataFrame, metadata: dict) -> DataFrame:
        """
        Based on TA indicators, populates the sell signal for the given dataframe
        :param dataframe: DataFrame populated with indicators
        :param metadata: Additional information, like the currently traded pair
        :return: DataFrame with buy column
        """
        dataframe.loc[(dataframe['signal'] == -1), 'sell'] = 1
        return dataframe
